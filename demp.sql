CREATE TABLE CLIENT_MASTER (
	ClientNo VARCHAR(7), 
	Name VARCHAR(15), 
	City VARCHAR(10), 
	Pincode INTEGER, 
	State VARCHAR(15), 
	BalDue INTEGER 
);  
CREATE TABLE PRODUCT_MASTER (
	ProductNo VARCHAR(7), 
	Description VARCHAR(20), 
	ProfitPercent INTEGER, 
	UnitMeasure VARCHAR(6), 
	QtyOnHand INTEGER,
	ReorderLvl INTEGER,
	SellPrice INTEGER,
	CostPrice INTEGER
); 
